#Exercise 1 - Basic Math
import math

#For a = 2.3, find the ceil and the floor of a.
a1 = 2.3
ceil_value = math.ceil(a1)
floor_value = math.floor(a1)
print(f"If a = {a1}, the ceil is: {ceil_value}")
print(f"If a = {a1}, the floor is: {floor_value}")

#For a = 5, find the factorial of a.
a2 = 5
factorial_value = math.factorial(a2)
print(f"If a = {a2}, the factorial is: {factorial_value}")

#Find the value of 2^3.
a3 = 2
cube_value = math.pow(a3, 3)
print(f"If a = {a3}, the cube root of {a3} is: {cube_value}")

#For a = 16, find the square root of a.
a4 = 16
sqrt_value = math.sqrt(a4)
print(f"If a = {a4}, the square root of {a4} is: {sqrt_value}")