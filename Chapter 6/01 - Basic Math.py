#Exercise 1 - Basic Math
import math

#For a = 2.3, find the ceil and the floor of a.
a = 2.3
ceil_value = math.ceil(a)
floor_value = math.floor(a)
print(f"The ceil of {a} is: {ceil_value}")
print(f"The floor of {a} is: {floor_value}")

#For a = 5, find the factorial of a.
a = 5
factorial_value = math.factorial(a)
print(f"The factorial of {a} is: {factorial_value}")

#Find the value of 2^3.
cube_value = 2 ** 3
print(f"The cube of 2 is: {cube_value}")

#For a = 16, find the square root of a.
a = 16
sqrt_value = math.sqrt(a)
print(f"The square root of {a} is: {sqrt_value}")